const I18nContext = createContext(null);

export function I18nProvider({ children }) {
  const stored = typeof window !== 'undefined' ? localStorage.getItem('agri_lang') : null;
  const [lang, setLangState] = useState(stored || 'en');

  const setLang = useCallback((code) => {
    setLangState(code);
    localStorage.setItem('agri_lang', code);
    document.documentElement.lang = code;
  }, []);

  const t = useCallback((key, fallback) => {
    return translations[lang]?.[key] || translations.en?.[key] || fallback || key;
  }, [lang]);

  return React.createElement(I18nContext.Provider, { value: { lang, setLang, t, LANGUAGES } }, children);
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) return { lang: 'en', setLang: () => {}, t: (k) => k, LANGUAGES };
  return ctx;
}

export default useI18n;
